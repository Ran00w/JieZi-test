def load_prompt_bundle(prompt_name: str, entry: dict[str, Any], image_record: dict[str, Any]) -> tuple[str, list[dict[str, Any]], str]:
    if prompt_name == "simple":
        return SIMPLE_SYSTEM_PROMPT, [], SIMPLE_USER_PROMPT
    if prompt_name == "complex":
        return COMPLEX_SYSTEM_PROMPT, COMPLEX_FEW_SHOTS, build_complex_user_payload()
    raise ValueError(f"unsupported prompt_name: {prompt_name}")
